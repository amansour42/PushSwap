#include "checker.h"

int	digit_test(char *s)
{
	int i;

	i = -1;
	while (s[++i])
	{
		if (!ft_isdigit(s[i]) && s[i] != ' ' && s[i] != '+' && s[i] != '-')
			return (0);
	}
	return (1);
}
